package Loop;

public class While146To150 {
	public static void main(String[] args) {
		
		System.out.println("main start");
		int i = 146;
		while(i<=150)
		{
			System.out.println(i);
			i++;
		}
		System.out.println("reverse order");
		
		int i1=150;
		while(i1>=146)
		{
			System.out.println(i1);
			i1--;
		}
		
		System.out.println("main end");
	}

}
