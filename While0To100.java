package Loop;

public class While0To100 {
	public static void main(String[] args) {
		System.out.println("main start");
		
		
		int i=0;
		while(i<=100)
		{
			System.out.println(i);
			i++;
		}
		System.out.println("reverse order");
		
		int i1 =100;
		while(i1>=0)
		{
			System.out.println(i1);
			i1--;
		}
		System.out.println("main end");
	}

}
