package Loop;

public class Numer {
	public static void main(String[] args) {
		
		System.out.println("main start");
		 for(int i=1; i<=5; i++)
		{
			System.out.println(i);
		} 
		 System.out.println("Now reverse");
		for(int i =5; i>=1; i--)
		{
			System.out.println(i);
		}
		
		System.out.println("main end");
	}

}
