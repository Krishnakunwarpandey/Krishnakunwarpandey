package Basics;

public class TypeCast {
	public static void main(String[] args) {
		System.out.println("main start");
		// byte to short 
		byte b=10;
		short s =b;
		System.out.println(s);
		System.out.println("=====");
		// int to long
		int i = 25;
		long l =i;
		System.out.println(l);
		System.out.println("=====");
		// int to double
		int i1 =30;
		double d=i1;
		System.out.println(d);
		System.out.println("=====");
		// char to int
		char c ='A';
		int a =c;
		System.out.println(a);
		System.out.println("main end");
		
	}

}
