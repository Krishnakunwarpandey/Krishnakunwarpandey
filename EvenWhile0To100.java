package Loop;

public class EvenWhile0To100 {
	public static void main(String[] args) {
		System.out.println("main start");
		
		int a =0;
		
		while(a<=100)
		{
			System.out.println(a);
		     	a=a+2;
		}
		System.out.println("reverse order");
		
		
		
		int b =100;
		
		while(b>=0)

		{
			System.out.println(b);
			b=b-2;
		}
	
	}

}
